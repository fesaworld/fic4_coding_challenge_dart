import 'package:dart_1_variable/dart_1_variable.dart' as dart_1_variable;

void main(List<String> arguments) {
  /*
    Buatlah sebuah program Dart yang
    menghitung luas persegi panjang. Program ini
    harus menggunakan variabel untuk
    menyimpan nilai panjang dan lebar, dan
    kemudian mencetak luasnya.
  */

  int panjang = 20;
  int lebar = 10;
  int hasil;

  hasil = panjang * lebar;

  print('luas persegi panjang dengan panjang: ${panjang}cm & lebar: ${lebar}cm adalah: ${hasil}cm2');
}
